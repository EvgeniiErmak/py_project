def addition():
    """Функция сложения двух чисел"""
    number_1 = int(input())
    number_2 = int(input())
    answer = number_1 + number_2
    return answer


def subtraction():
    """Функция вычитания двух чисел"""
    number_1 = int(input())
    number_2 = int(input())
    answer_2 = number_1 - number_2
    return answer_2